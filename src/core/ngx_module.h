//nginx模块类型结构体
struct ngx_module_s {
    //模块的下标，ctx_index表达了该模块再一类模块的顺序和优先级，例如http、event模块
    //由核心模块ngx_http_module设置
    ngx_uint_t            ctx_index;
    //index表示存放在ngx_modules数组中的序号，也表示了再所有模块中的序号
    ngx_uint_t            index;
    //模块的名字
    char                 *name;
    //spare系列的保留变量，暂未使用
    ngx_uint_t            spare0;
    ngx_uint_t            spare1;
    //模块的版本，便于将来的扩展。目前只有一种，默认为1
    ngx_uint_t            version;
    const char           *signature;
    /**
     * ctx用于指向一类模块的上下文结构体，为什么需要ctx呢?因为前面说过，Nginx模块有许多种类，不同类模块之间的功能差别很大。
     * 例如，事件类型的模块主要处理I/O事件相关的功能，HTTP类型的模块主要处理HTTP应用层的功能。
     * 这样，每个模块都有了自己的特性，而ctx将会指向特定类型模块的公共接口。例如，在HTTP模块中，ctx需要指向ngx_http_module_t结构体
     */
    void                 *ctx;
    //commands将处理nginx.conf中的配置项
    ngx_command_t        *commands;
    /**
     * 表示该模块的类型，它与ctx指针是紧密相关的。在官方Nginx中，它的取值范围是以下5种:
     * NGX_HTTP_MODULE、NGX_CORE_MODULE、NGX_CONF_MODULE、NGX_EVENT_MODULE、NGX_MAIL_MODULE。这
     */
    ngx_uint_t            type;

    /**
     * 下面7个函数是nginx启动和停止过程中会调用的函数，代表了nginx的生命周期
     * 如果模块不需要关注这些，设置为NULL即可
     */
     //master进程启动时回调函数，但是现在的master进程中并没回到，这个函数也没有作用
    ngx_int_t           (*init_master)(ngx_log_t *log);
    //周期初始化所有模块时被调用。在master/worker模式下，这个阶段会在启动worker子进程前完成
    ngx_int_t           (*init_module)(ngx_cycle_t *cycle);
    //init_process回调方法在正常服务前被调用。在master/worker模式下，
    //多个worker子进程已经产生，在每个worker进程的初始化过程会调用所有模块的init_process函数
    ngx_int_t           (*init_process)(ngx_cycle_t *cycle);
    //初始化线程时调用，但nginx不支持多线程，所以没有使用此函数
    ngx_int_t           (*init_thread)(ngx_cycle_t *cycle);
    //线程退出时调用，由于nginx不支持多线程，所以也没有使用
    void                (*exit_thread)(ngx_cycle_t *cycle);
    //worker进程退出前调用，需要在master/worker模式
    void                (*exit_process)(ngx_cycle_t *cycle);
    //master进程退出前调用
    void                (*exit_master)(ngx_cycle_t *cycle);
    /**
     * 下面8个字段都是保留字段，暂无使用
     */
    uintptr_t             spare_hook0;
    uintptr_t             spare_hook1;
    uintptr_t             spare_hook2;
    uintptr_t             spare_hook3;
    uintptr_t             spare_hook4;
    uintptr_t             spare_hook5;
    uintptr_t             spare_hook6;
    uintptr_t             spare_hook7;
};


